#include "ft_printf.h"

int	ft_putnbr(int nb)
{
	char			c;
	unsigned int	i;

	i = 0;
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
		i++;
	}
	if (nb >= 10)
		i += ft_putnbr(nb / 10);
	c = '0' + nb % 10;
	write(1, &c, 1);
	i++;
	return (i);
}
